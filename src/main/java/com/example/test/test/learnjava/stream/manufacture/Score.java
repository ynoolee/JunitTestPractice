package com.example.test.test.learnjava.stream.manufacture;

public class Score {
	private int kor;
	private int eng;
	private int math;

	public Score(int kor, int eng, int math) {
		this.kor = kor;
		this.eng = eng;
		this.math = math;
	}

	public int getKor() {
		return kor;
	}

	public int getEng() {
		return eng;
	}

	public int getMath() {
		return math;
	}
}
